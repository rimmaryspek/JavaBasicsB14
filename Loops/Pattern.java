
public class Pattern {

	public static void main(String[] args) {
		// Pattern 5 row and 5 column's 

		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				System.out.print("$ ");
			}
			System.out.println();
		}
	
		for (int t = 1; t <= 5; t++) { // row
			for (int o = 1; o <= 5; o++) { // column's
				System.out.print(t+  " ");
			}
			System.out.println();	
	
	
	}

}
}