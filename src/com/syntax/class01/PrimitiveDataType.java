package com.syntax.class01;

public class PrimitiveDataType {

	public static void main(String[] args) {
		// Whole numbers
		byte bag1=20;
		short bag2=2000;
		int bag3=200000;
		long bag4=20000000l;
		
		// Decimal Values 
		
		float value1=2.22f;
		double value2=22.22;
		
        // Single characters 
		
		char fleshcard1='W';
		char fleshcard2='$';
		
		// Yes or No values
		
		boolean rain=true;
		boolean sun=false;
		
		System.out.println(bag1);
		System.out.println(bag2);
		System.out.println(bag3);
		System.out.println(bag4);
		
		System.out.println(value1);
		System.out.println(value2);
		
		System.out.println(fleshcard1);
		System.out.println(fleshcard2);
		
		System.out.println(rain);
		System.out.println(sun);
		
		
	}

}
