package Ifelseclass3;

public class RecapOperations {

	public static void main(String[] args) {
	/*Addition=> +
	 * subtractions=> -
	 * Multiplication => *
	 * Division => /
	 * Modulus => % 
	 * 
	 */
	int n1=9;
	int n2=2;
	System.out.println(n1/n2);//dividing n1 and n2
	
	double n3=9;
	double n4=2;
	System.out.println(n3/n4);//dividing n3 and n4
	
	double n5=1.0;
	double n6=3.0;
	System.out.println(n5/n6);//dividing n5 and n61
	
	
	
	}

}
