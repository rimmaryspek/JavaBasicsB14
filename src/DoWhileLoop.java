
public class DoWhileLoop {

	public static void main(String[] args) {
// Do while loop
		/*
		 * 
		 */

		int n = 1;
		while (n <= 2) {
			System.out.println("i am while loop");
			n++;
		}

		System.out.println("------------");

		int n1 = 1;

		do {
			System.out.println("i am do while loop");
			n1++;

		} while (n1 >= 2);

	}

}